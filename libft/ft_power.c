#include "includes/libft.h"

size_t	ft_power(size_t nb, size_t p)
{
	size_t	res;

	res = nb;
	if (p == 0)
		return (1);
	while (p > 1)
	{
		res *= nb;
		p--;
	}
	return (res);
}
